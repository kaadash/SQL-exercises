SELECT NAZWISKO, ZATRUDNIONY, ID_ZESP
FROM PRACOWNICY
WHERE (ZATRUDNIONY, ID_ZESP) IN
  (SELECT MAX(ZATRUDNIONY), ID_ZESP
    FROM PRACOWNICY
    GROUP BY ID_ZESP);
