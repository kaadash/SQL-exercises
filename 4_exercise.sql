SELECT NAZWISKO, ETAT, ZATRUDNIONY
FROM PRACOWNICY
WHERE ZATRUDNIONY=
  (SELECT MIN(ZATRUDNIONY)
    FROM PRACOWNICY
    WHERE ETAT='PROFESOR')
AND ETAT='PROFESOR';
