SELECT NAZWISKO
FROM PRACOWNICY p
WHERE NOT EXISTS (
  SELECT ID_PRAC FROM PRACOWNICY
  WHERE ID_SZEFA=p.ID_PRAC AND ETAT='STAZYSTA')
AND p.ETAT='PROFESOR';

